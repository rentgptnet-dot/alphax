def get_sector(symbol: str) -> str:
    """從股票代號回傳產業類別（用於公開模式匿名化）。"""
    code = symbol.split(".")[0] if "." in symbol else symbol
    return SECTOR_MAP.get(code, "其他")


def anonymize_picks(picks: list) -> list:
    """
    把 picks list 匿名化：原本 [{"symbol": "2330.TW", ...}, ...]
    變成 [{"display": "半導體 #1", "sector": "半導體", ...}, ...]
    同產業按出現順序編號。
    """
    sector_counter: dict = {}
    out = []
    for p in picks:
        sym = p.get("symbol", "")
        sector = get_sector(sym)
        sector_counter[sector] = sector_counter.get(sector, 0) + 1
        n = sector_counter[sector]
        new = dict(p)
        new["display"] = f"{sector} #{n}"
        new["sector"] = sector
        new.pop("symbol", None)
        new.pop("name", None)
        out.append(new)
    return out


